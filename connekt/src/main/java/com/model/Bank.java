package com.model;

public class Bank {
	
	private int code;
	private String name;
	private String kana;
	private String hira;
	private String roma;
	
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getKana() {
		return kana;
	}
	public void setKana(String kana) {
		this.kana = kana;
	}
	public String getHira() {
		return hira;
	}
	public void setHira(String hira) {
		this.hira = hira;
	}
	public String getRoma() {
		return roma;
	}
	public void setRoma(String roma) {
		this.roma = roma;
	}
}
